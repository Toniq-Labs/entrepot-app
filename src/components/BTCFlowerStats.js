export function getTraits(index) {
    return btcFlowerTraits[index];
}
export function getPairing(BTCFlower) {
    let flower = BTCFlower.flower;
    let coin = BTCFlower.coin;
    let grave = BTCFlower.grave.split('_').pop();
    if (flower === coin && coin === grave) {
        return 'triple';
    } else if (flower === coin || flower === grave || grave === coin) {
        return 'double';
    } else {
        return 'none';
    }
}
